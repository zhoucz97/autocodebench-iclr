using System;
using System;
using System.Text;
using System.Collections.Generic;
using System.Diagnostics;

class Program
{


/* This function takes a string as input and returns a 'Close Friend' string based on specific ASCII value manipulations.
   The Close Friend string is generated by summing the ASCII values of each pair of adjacent characters in the input string,
   creating a new character from the sum. For the last character of the Close Friend string, sum the ASCII value of the 
   last and first characters of the input string. The input string length should be between 2 and 100 characters, and 
   each character's ASCII value should not exceed 63.

   Example:
   >>> GenerateCloseFriendString("1234")
   "cege"

*/
    static string GenerateCloseFriendString(string inputString)
{
        // Check if the input string length is within the valid range
        if (inputString.Length < 2 || inputString.Length > 100)
        {
            throw new ArgumentException("Input string length must be between 2 and 100 characters.");
        }

        // Check if any character's ASCII value exceeds 63
        foreach (char c in inputString)
        {
            if (c > 63)
            {
                throw new ArgumentException("Each character's ASCII value should not exceed 63.");
            }
        }

        char[] closeFriendChars = new char[inputString.Length];

        // Process each pair of adjacent characters
        for (int i = 0; i < inputString.Length - 1; i++)
        {
            int sum = (int)inputString[i] + (int)inputString[i + 1];
            closeFriendChars[i] = (char)sum;
        }

        // Handle the last character by summing the last and first characters
        int lastSum = (int)inputString[inputString.Length - 1] + (int)inputString[0];
        closeFriendChars[inputString.Length - 1] = (char)lastSum;

        // Convert the character array to a string and return it
        return new string(closeFriendChars);
    }
    static void Main()
    {
        Debug.Assert(GenerateCloseFriendString("1234") == "cege");
        Debug.Assert(GenerateCloseFriendString("4312") == "gdcf");

    }
}